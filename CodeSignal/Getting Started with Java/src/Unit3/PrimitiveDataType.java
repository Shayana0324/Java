package Unit3;

public class PrimitiveDataType {
        public static void main(String[] args) {
            // Declare a float variable to store the distance from Earth to the Sun in kilometers         - 149600000.0 kilometers
            float distance = 149600000.0f;          // Distance from earth to the Sun in km

            // Declare an int variable to store the number of days Earth takes to orbit the Sun - 365 days
            int numberOfDays = 365;                 // Number of days Earth takes to orbit the Sun

            // Print the distance of Earth from the Sun followed by " km from the Sun."
            System.out.println("Distance of Earth from the Sun " + distance + " km from the Sun.");

            // Print the number of days Earth takes to orbit the Sun, followed by " days to orbit the Sun."
            System.out.println("Number of days Earth takes to orbit the Sun " + numberOfDays + " days to orbit the Sun.");

            // Declare a boolean variable to store the truth about whether space is dark - it is!
            boolean space = true;

            // Use a char variable to represent the first letter of 'Cosmos' - C
            char cosmos = 'C';
        }
    }

